int		ft_sqrt(int nb)
{
	int		res;
	int		a;

	res = 0;
	a = 1;
	while (a != nb && nb > 0 && a <= 46340)
	{
		res = a * a;
		if (res == nb)
		{
			return (a);
		}
		a++;
	}
	return (0);
}
