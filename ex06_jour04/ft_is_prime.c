int		ft_is_prime(int nb)
{
	int		a;
	int		res;

	res = 0;
	a = 1;
	while (nb > 0 && res < nb && a <= 46340)
	{
		res = a * a;
		a++;
	}
	res = 3;
	while (res < a)
	{
		if (nb % res == 0 || nb <= 1)
		{
			return (0);
		}
		res += 2;
	}
	return (1);
}
